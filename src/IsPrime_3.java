public class IsPrime_3 {
    public static void main(String args[]) {
        int i, j, cnt = 1;
        for (i = 1; i <= 10; i++) {
            for (j = 1; j <= 10; j++) {
                System.out.print(cnt+" ");
                cnt++;
            }

            System.out.println();
        }
    }
}
